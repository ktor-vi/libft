int	ft_isalnum(int c)
{
	int	result;

	result = 1;
	if (!((c >= 97 && c <= 122) || (c >= 65 && c <= 90)) && !(c >= 48
			&& c <= 57))
	{
		result = 0;
	}
	return (result);
}

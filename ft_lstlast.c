#include "libft.h"
#include <stdlib.h>

t_list	*ft_lstlast(t_list *lst)
{
	struct s_list	*ptr;

	ptr = lst;
	if (ptr == NULL)
		return (NULL);
	while (ptr->next != NULL)
		ptr = ptr->next;
	return (ptr);
}
